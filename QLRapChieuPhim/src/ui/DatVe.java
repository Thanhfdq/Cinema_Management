    void xoaVe() {
        int[] ve = tblDonMuaVe.getSelectedRows();
        for (int i = ve.length - 1; i > -1; i--) {
            for (int j = dsDatVe.size() - 1; j > -1; j--) {
                if (ve[i] == j) {
                    dsDatVe.remove(j);
                    break;
                }
            }
        }
        refreshDonMuaVe();
    }

