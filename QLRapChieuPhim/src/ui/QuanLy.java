    void deletDsPC() {
        int[] rows = tblDanhSachPC.getSelectedRows();
        if (rows.length > 0
                && MsgBox.confirm(this, "Bạn muốn xóa phòng chiếu này?")) {
            for (int row : rows) {
                String mapc = (String) tblDanhSachPC.getValueAt(row, 0);
                daoPC.delete(mapc);
                this.fillTablePC();
            }
        }
    }

