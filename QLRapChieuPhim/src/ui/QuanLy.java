    void editPC() {
        String mapc = (String) tblDanhSachPC.getValueAt(this.rowPC, 0);
        PhongChieu pc = daoPC.selectById(mapc);
        this.setFormPC(pc);
    }

    void firstPC() {
        this.rowPC = 0;
        this.editPC();
        MsgBox.alert(this, "Bạn đang ở đầu danh sách");
    }

    void backPC() {
        if (this.rowPC > 0) {
            this.rowPC--;
            this.editPC();
        }
    }

    void nextPC() {
        if (this.rowPC < tblDanhSachPC.getRowCount() - 1) {
            this.rowPC++;
            this.editPC();
        }
    }

    void lastPC() {
        this.rowPC = tblDanhSachPC.getRowCount() - 1;
        this.editPC();
        MsgBox.alert(this, "Bạn đang ở cuối danh sách");
    }

